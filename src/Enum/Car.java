package Enum;

public enum Car {
    SMART(1,"smart"),
    SUV(2,"suv");

    private Integer code;
    private String name;

    Car(Integer code, String name) {
        this.code = code;
        this.name = name;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
